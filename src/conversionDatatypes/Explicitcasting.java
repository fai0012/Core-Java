package conversionDatatypes;

public class Explicitcasting {
public static void main(String[]args) {
	double a = 50122.502525252525;
	System.out.println("double Representation:"+a);
	float f = (float)a;
	System.out.println("float Representation:"+f);
	long b = (long)a;
	System.out.println("long Representaion:"+b);
	int c = (int)b;
	System.out.println("int Representation:"+c);
}
}
